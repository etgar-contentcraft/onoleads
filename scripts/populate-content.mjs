/* ──────────────────────────────────────────────────────────────
 * Main update logic
 * ────────────────────────────────────────────────────────────── */

async function supabaseGet(path) {
  const res = await fetch(`${SUPABASE_URL}/rest/v1/${path}`, { headers: { apikey: SUPABASE_KEY, Authorization: `Bearer ${SUPABASE_KEY}` } });
  if (!res.ok) throw new Error(`GET ${path}: ${res.status} ${await res.text()}`);
  return res.json();
}

async function supabasePatch(table, id, body) {
  const res = await fetch(`${SUPABASE_URL}/rest/v1/${table}?id=eq.${id}`, {
    method: "PATCH",
    headers,
    body: JSON.stringify(body),
  });
  if (!res.ok) throw new Error(`PATCH ${table}/${id}: ${res.status} ${await res.text()}`);
}

async function main() {
  console.log("=== Ono Content Population Script ===\n");

  // 1. Get all published pages
  const pages = await supabaseGet("pages?select=id,slug,title_he&status=eq.published&order=slug");
  console.log(`Found ${pages.length} published pages.\n`);

  let updated = 0;
  let skipped = 0;
  let errors = 0;

  for (const page of pages) {
    const slug = page.slug;
    const program = PROGRAMS[slug];

    if (!program) {
      console.log(`  SKIP: ${slug} (no program data)`);
      skipped++;
      continue;
    }

    console.log(`  UPDATE: ${slug} — ${program.name}`);

    try {
      // Get sections for this page
      const sections = await supabaseGet(`page_sections?select=id,section_type,content&page_id=eq.${page.id}&order=sort_order`);

      for (const section of sections) {
        const newContent = buildSectionContent(section.section_type, program, section.content || {});

        // Only update if content actually changed
        if (JSON.stringify(newContent) !== JSON.stringify(section.content)) {
          await supabasePatch("page_sections", section.id, { content: newContent });
        }
      }

      updated++;
    } catch (err) {
      console.error(`  ERROR: ${slug} — ${err.message}`);
      errors++;
    }
  }

  console.log(`\n=== Done ===`);
  console.log(`Updated: ${updated}, Skipped: ${skipped}, Errors: ${errors}`);
}

main().catch(err => {
  console.error("Fatal error:", err);
  process.exit(1);
});
