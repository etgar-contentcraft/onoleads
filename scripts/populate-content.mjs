        subheading_he: isHebrew ? p.heroSub : existingContent.subheading_he,
        subheading_en: !isHebrew ? p.heroSub : undefined,
        stat_value: p.statValue || existingContent.stat_value,
        stat_label_he: isHebrew ? p.statLabel : existingContent.stat_label_he,
