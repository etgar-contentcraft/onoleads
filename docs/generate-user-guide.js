const FONT_SIZE_BODY = 22;
const FONT_SIZE_TABLE = 20;
const FONT_SIZE_SMALL = 18;

/* ────────────────────────── Helpers ────────────────────────── */

/** Heading with Hebrew RTL formatting and outline level */
function heading(text, level = HeadingLevel.HEADING_1) {
  return new Paragraph({
    heading: level,
    children: [new TextRun({ text, font: FONT_MAIN, rightToLeft: true })],
    spacing: { before: 400, after: 200 },
    bidirectional: true,
    alignment: AlignmentType.RIGHT,
  });
}

/** Chapter title — larger than H1, brand colored, centered */
function chapterTitle(number, title) {
  return [
    new Paragraph({
      children: [new TextRun({
        text: `פרק ${number}`,
        size: 28, bold: true, color: "6B7A00", font: FONT_MAIN, rightToLeft: true
      })],
      alignment: AlignmentType.RIGHT, bidirectional: true,
      spacing: { before: 600, after: 80 },
    }),
    new Paragraph({
      heading: HeadingLevel.HEADING_1,
      children: [new TextRun({ text: title, font: FONT_MAIN, rightToLeft: true, color: "1A1A2E" })],
      alignment: AlignmentType.RIGHT, bidirectional: true,
      spacing: { after: 240 },
    }),
    new Paragraph({
      children: [new TextRun({ text: "", size: 4 })],
      border: { bottom: { style: BorderStyle.SINGLE, size: 18, color: "B8D900" } },
      spacing: { after: 240 },
    }),
  ];
}

/** Normal body paragraph (Hebrew RTL) */
function para(text, opts = {}) {
  const runs = typeof text === "string"
    ? [new TextRun({ text, size: FONT_SIZE_BODY, font: FONT_MAIN, rightToLeft: true, ...opts })]
    : text;
  return new Paragraph({
    children: runs,
    spacing: { after: 140, line: 320 },
    bidirectional: true,
    alignment: AlignmentType.RIGHT,
  });
}

/** Bold paragraph for emphasis */
function boldPara(text) {
  return para([new TextRun({ text, bold: true, size: FONT_SIZE_BODY, font: FONT_MAIN, rightToLeft: true })]);
}

/** Bullet list item using numbering config */
function bullet(text, level = 0) {
  return new Paragraph({
    children: [new TextRun({ text, size: FONT_SIZE_BODY, font: FONT_MAIN, rightToLeft: true })],
    numbering: { reference: "heBullets", level },
    spacing: { after: 80, line: 300 },
    bidirectional: true,
    alignment: AlignmentType.RIGHT,
  });
}

/** Numbered step using numbering config */
function step(text, level = 0) {
  return new Paragraph({
    children: [new TextRun({ text, size: FONT_SIZE_BODY, font: FONT_MAIN, rightToLeft: true })],
    numbering: { reference: "heSteps", level },
    spacing: { after: 100, line: 300 },
    bidirectional: true,
    alignment: AlignmentType.RIGHT,
  });
}

/** Callout box (tip / warning / note) rendered as single-cell table */
function callout(label, text, fillColor = CALLOUT_FILL) {
  return new Table({
    rows: [new TableRow({
      children: [new TableCell({
        children: [
          new Paragraph({
            children: [new TextRun({
              text: label, bold: true, size: FONT_SIZE_BODY,
              font: FONT_MAIN, rightToLeft: true, color: "6B5100"
            })],
            bidirectional: true, alignment: AlignmentType.RIGHT,
            spacing: { after: 80 },
          }),
          new Paragraph({
            children: [new TextRun({
              text, size: FONT_SIZE_BODY, font: FONT_MAIN, rightToLeft: true
            })],
            bidirectional: true, alignment: AlignmentType.RIGHT,
          }),
        ],
        shading: { type: ShadingType.CLEAR, fill: fillColor },
        borders: {
          top: { style: BorderStyle.SINGLE, size: 12, color: "E0C066" },
          bottom: { style: BorderStyle.SINGLE, size: 12, color: "E0C066" },
          left: { style: BorderStyle.SINGLE, size: 24, color: "E0C066" },
          right: { style: BorderStyle.SINGLE, size: 24, color: "E0C066" },
        },
        margins: { top: 180, bottom: 180, left: 240, right: 240 },
        width: { size: CONTENT_WIDTH, type: WidthType.DXA },
      })],
    })],
    width: { size: CONTENT_WIDTH, type: WidthType.DXA },
    columnWidths: [CONTENT_WIDTH],
    visuallyRightToLeft: true,
  });
}

/** Two-column table with header row. RTL-aware. */
function simpleTable(headers, rows, colRatios) {
  const colCount = headers.length;
  const ratios = colRatios || Array(colCount).fill(1);
  const totalRatio = ratios.reduce((a, b) => a + b, 0);
  const columnWidths = ratios.map(r => Math.floor((CONTENT_WIDTH * r) / totalRatio));
  // adjust last col for rounding
  columnWidths[colCount - 1] = CONTENT_WIDTH - columnWidths.slice(0, -1).reduce((a, b) => a + b, 0);

  const headerRow = new TableRow({
    children: headers.map((h, i) => new TableCell({
      children: [new Paragraph({
        children: [new TextRun({
          text: h, bold: true, size: FONT_SIZE_TABLE,
          font: FONT_MAIN, rightToLeft: true, color: HEADER_TEXT
        })],
        alignment: AlignmentType.RIGHT, bidirectional: true,
      })],
      shading: { type: ShadingType.CLEAR, fill: HEADER_FILL },
      borders: BORDERS,
      width: { size: columnWidths[i], type: WidthType.DXA },
      margins: CELL_MARGINS,
    })),
    tableHeader: true,
  });

  const dataRows = rows.map((row, rowIdx) => new TableRow({
    children: row.map((cell, colIdx) => new TableCell({
      children: [new Paragraph({
        children: [new TextRun({
          text: String(cell), size: FONT_SIZE_TABLE, font: FONT_MAIN, rightToLeft: true
        })],
        alignment: AlignmentType.RIGHT, bidirectional: true,
      })],
      borders: BORDERS,
      width: { size: columnWidths[colIdx], type: WidthType.DXA },
      margins: CELL_MARGINS,
      shading: rowIdx % 2 === 1
        ? { type: ShadingType.CLEAR, fill: ALT_ROW_FILL }
        : undefined,
    })),
  }));

  return new Table({
    rows: [headerRow, ...dataRows],
    width: { size: CONTENT_WIDTH, type: WidthType.DXA },
    columnWidths,
    visuallyRightToLeft: true,
  });
}

/** Small spacer paragraph */
function spacer(before = 0, after = 120) {
  return new Paragraph({ spacing: { before, after }, children: [] });
}

/** Page break */
function pageBreak() {
  return new Paragraph({ children: [new PageBreak()] });
}

