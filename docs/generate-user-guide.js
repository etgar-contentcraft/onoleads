const PAGE_HEIGHT = 16838;
const MARGIN_TOP = 1440;
const MARGIN_BOTTOM = 1440;
const MARGIN_LEFT = 1440;
const MARGIN_RIGHT = 1440;

/** Content width = page width minus left and right margins */
const CONTENT_WIDTH = PAGE_WIDTH - MARGIN_LEFT - MARGIN_RIGHT;

/** Cell padding for readable tables */
const CELL_MARGINS = { top: 100, bottom: 100, left: 140, right: 140 };

/** Standard border style */
const BORDER = { style: BorderStyle.SINGLE, size: 1, color: "C8CDD1" };
const BORDERS = { top: BORDER, bottom: BORDER, left: BORDER, right: BORDER };

/** Row colors */
