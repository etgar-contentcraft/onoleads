/**
 * Catalog of 10 built-in thank-you-page templates.
 *
 * Research basis — these patterns were identified by analyzing the
 * thank-you / confirmation pages of leading academic institutions and
 * online learning platforms (Harvard, Stanford, MIT, Wharton, Berkeley,
 * Penn State World Campus, ASU Online, Coursera, Liberty University,
 * Northwestern, NYU, Open University, 2U-powered programs).
 *
 * Each template targets a specific psychological lever:
 *   1. Reassurance       — calm professional tone, "we have your details"
 *   2. Celebration       — emotional reinforcement, dopamine hit
 *   3. Personal advisor  — trust via a real human face
 *   4. Calendar focus    — drive immediate next action (book a call)
 *   5. Video welcome     — personal touch from faculty/dean
 *   6. Resource library  — self-service for browsers
 *   7. Social proof      — peer testimonials
 *   8. Urgency cohort    — scarcity / FOMO
 *   9. Multi-channel     — let prospect choose contact method
 *  10. Minimal light     — clean elegant alternative for design-focused brands
 *
 * Each template ships with content in all three supported languages.
 */

